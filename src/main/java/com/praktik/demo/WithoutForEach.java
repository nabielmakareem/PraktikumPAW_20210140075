package com.tugas.tugas;

public class WithoutForEach {
    public static void main(String[] args) {
        String[] array = {
                "Eko", "Kurniawan", "Khannedy",
                "Programmer", "Zaman", "Now"
        };

        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}
